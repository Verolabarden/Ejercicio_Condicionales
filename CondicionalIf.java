package paqprincipal;

public class CondicionalIf {
    public static void main(String[] args) {
        int numerolf = 7;
        if (numerolf == 0){
            System.out.println("Es cero");
        } else if (numerolf > 0) {
            System.out.println("Es positivo");
        } else {
            System.out.println("Es negativo");
        }
    }
}
